export function getCurrentLanguage() {
    return (localStorage.getItem(STORAGE_KEY) || 'ES').toUpperCase();
}

export function setCurrentLanguage(code) {
    localStorage.setItem(STORAGE_KEY, (code || 'ES').toUpperCase());
}

export function t(key, fallback = '') {
    const lang = getCurrentLanguage();
    return DICTIONARY[lang]?.[key] || DICTIONARY.ES?.[key] || fallback || key;
}
